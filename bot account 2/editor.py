"""
editor.py — Агент-редактор.

Вход:  str (черновик от Writer)
Выход: str (финальный пост)
"""

import logging
from gemini_utils import gemini_generate

logger = logging.getLogger(__name__)

# ──────────────────────────────────────────────
# AGENT IDENTITY  (edit freely)
# ──────────────────────────────────────────────
SYSTEM_INSTRUCTION = (
    "Ты — главный редактор. "
    "Твоя задача: сбалансировать материал. "
    "Соблюдай пропорцию 70/30 (70% эмоций и интереса, 30% взвешенной логики). "
    "Если Writer перегнул с провокацией — добавь рациональный аргумент или второй угол зрения. "
    "Не допускай «желтизны», только качественная интеллектуальная провокация."
)

PROMPT_TEMPLATE = """
Перед тобой черновик поста от Writer-а. Твоя задача — довести его до финального состояния.

КРИТЕРИИ РЕДАКТУРЫ:
- Пропорция 70% (эмоции / интерес) / 30% (логика / факты) должна соблюдаться.
- Убери "жёлтые" формулировки — замени их на интеллектуальную провокацию.
- Если в черновике нет второго угла зрения — добавь одно взвешенное суждение.
- Сохрани структуру: заголовок, хук, тело, финальный вопрос.
- Не удлиняй текст — только улучшай качество.

Черновик:
{draft}

Верни только финальный текст поста, без комментариев и пояснений.
"""
# ──────────────────────────────────────────────


async def edit(draft: str) -> str:
    """
    Принимает черновик от Writer, возвращает финальный пост (str).
    """
    logger.info("[Editor] Редактирую пост...")

    final = await gemini_generate(
        prompt=PROMPT_TEMPLATE.format(draft=draft),
        system_instruction=SYSTEM_INSTRUCTION,
        temperature=0.5,
        top_p=0.9,
    )

    logger.info("[Editor] Финальная версия готова.")
    return final